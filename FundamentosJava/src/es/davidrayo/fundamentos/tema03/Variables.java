package es.davidrayo.fundamentos.tema03;

public class Variables {

	public static void main(String[] args) {
		/// número enteros
		byte numero1 = 10;
		short numero2 = 12;
		int numero3 = 125;
		long numero4 = 243;

		// números decimales
		float numero5 = 12.5f;
		double numero6 = 32.4;

		// variables textuales
		char letra = 'A';
		String texto = "David";

		// variable booleana
		boolean isMayor = true;
	}
}
